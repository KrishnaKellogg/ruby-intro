# Arrays are lists of things
favorite_foods = ["tacos", "burritos", "nachos"]
puts favorite_foods

numbers = [4, 6, 8, 10]
puts numbers.inspect

# Accessing the array
puts favorite_foods [0]

# Add to the array
favorite_foods.push("more tacos")
puts favorite_foods.inspect

